import { useSession } from '@/hooks/useSession';
import { theme } from '@/lib/mui';
import { useSessionSocket } from '@/hooks/useSocket';
import { channelListAtom } from '@/stores/jotai';
import { useLogout } from '@/features/user/api/userLogin';

